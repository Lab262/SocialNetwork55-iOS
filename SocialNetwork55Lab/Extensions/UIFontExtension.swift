import UIKit

extension UIFont {
    
    class func gothamBook(size:CGFloat) -> UIFont {
        return UIFont(name: "Gotham Book", size: size)!
    }
    
    class func gothamBold(size:CGFloat) -> UIFont {
        return UIFont(name: "Gotham Bold", size: size)!
    }
    
    class func aleoRegular(size:CGFloat) -> UIFont {
        return UIFont(name: "Aleo-Regular", size: size)!
    }
    
    class func bebasNeue(size:CGFloat) -> UIFont {
        return UIFont(name: "BebasNeue", size: size)!
    }
}
